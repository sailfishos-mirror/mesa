import argparse
import sys

from mako import exceptions
from mako.template import Template

from gen_opcodes import OPCODES, Props

PROP_TO_C = {
    Props.HAS_JIP: 'GEN_OPCODE_PROP_HAS_JIP',
    Props.HAS_UIP: 'GEN_OPCODE_PROP_HAS_UIP',
    Props.BRANCH_CTRL: 'GEN_OPCODE_PROP_BRANCH_CTRL',
    Props.NO_DST: 'GEN_OPCODE_PROP_NO_DST',
}


def c_props(props: Props) -> str:
    bits = [c for prop, c in PROP_TO_C.items() if props & prop]
    return ' | '.join(bits) if bits else '0'


PUBLIC_HEADER_TEMPLATE = """/*
 * Copyright © 2026 Intel Corporation
 * SPDX-License-Identifier: MIT
 *
 * Autogenerated file, do not edit!
 */

#pragma once

#include \"util/macros.h\"

typedef enum ENUM_PACKED gen_opcode {
% for opcode in opcodes:
   GEN_OP_${opcode.enum_name},
% endfor
   GEN_NUM_OPCODES,
} gen_opcode;
"""

PRIVATE_HEADER_TEMPLATE = """/*
 * Copyright © 2026 Intel Corporation
 * SPDX-License-Identifier: MIT
 *
 * Autogenerated file, do not edit!
 */

#pragma once

#include <stdint.h>

enum gen_opcode_props {
   GEN_OPCODE_PROP_HAS_JIP     = 1 << 0,
   GEN_OPCODE_PROP_HAS_UIP     = 1 << 1,
   GEN_OPCODE_PROP_BRANCH_CTRL = 1 << 2,
   GEN_OPCODE_PROP_NO_DST      = 1 << 3,
};

struct gen_opcode_desc {
   const char *name;
   gen_format format;
   uint8_t num_srcs;
   uint16_t props;
};

extern const struct gen_opcode_desc gen_opcode_descs[GEN_NUM_OPCODES];

static inline const struct gen_opcode_desc *
gen_opcode_get_desc(gen_opcode op)
{
   return op < GEN_NUM_OPCODES ? &gen_opcode_descs[op] :
                                 &gen_opcode_descs[GEN_OP_ILLEGAL];
}

static inline gen_format
gen_opcode_format(gen_opcode op)
{
   return gen_opcode_get_desc(op)->format;
}

static inline unsigned
gen_opcode_num_srcs(gen_opcode op)
{
   return gen_opcode_get_desc(op)->num_srcs;
}

static inline bool
gen_opcode_has_prop(gen_opcode op, enum gen_opcode_props prop)
{
   return gen_opcode_get_desc(op)->props & prop;
}

static inline bool
gen_opcode_has_dst(gen_opcode op)
{
   return !gen_opcode_has_prop(op, GEN_OPCODE_PROP_NO_DST);
}
"""

CODE_TEMPLATE = """/*
 * Copyright © 2026 Intel Corporation
 * SPDX-License-Identifier: MIT
 *
 * Autogenerated file, do not edit!
 */

#include \"gen_private.h\"
#include \"gen_names.h\"

#include <assert.h>
#include <stddef.h>
#include <string.h>

const struct gen_opcode_desc gen_opcode_descs[GEN_NUM_OPCODES] = {
% for opcode in opcodes:
   { \"${opcode.name}\", ${opcode.format}, ${opcode.num_srcs}, ${c_props(opcode.props)} },
% endfor
};

const char *
gen_opcode_to_string(gen_opcode op)
{
   return op < GEN_NUM_OPCODES ? gen_opcode_descs[op].name : NULL;
}

gen_opcode
gen_opcode_from_string(const char *str, int size, bool *valid)
{
   assert(valid);

   for (unsigned i = 0; i < GEN_NUM_OPCODES; i++) {
      const char *name = gen_opcode_descs[i].name;
      if (size >= 0 && strlen(name) == (size_t)size &&
          memcmp(name, str, size) == 0) {
         *valid = true;
         return (gen_opcode)i;
      }
   }

   *valid = false;
   return GEN_OP_ILLEGAL;
}
"""


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument('--public-header', action='store', default=None)
    parser.add_argument('--private-header', action='store', default=None)
    parser.add_argument('--code', action='store', default=None)
    args = parser.parse_args()

    if not (args.public_header or args.private_header or args.code):
        parser.error('At least one output is required')

    try:
        template_input = {
            'opcodes': OPCODES,
            'c_props': c_props,
        }
        if args.public_header is not None:
            with open(args.public_header, 'w', encoding='utf-8') as f:
                f.write(Template(PUBLIC_HEADER_TEMPLATE).render(**template_input))
        if args.private_header is not None:
            with open(args.private_header, 'w', encoding='utf-8') as f:
                f.write(Template(PRIVATE_HEADER_TEMPLATE).render(**template_input))
        if args.code is not None:
            with open(args.code, 'w', encoding='utf-8') as f:
                f.write(Template(CODE_TEMPLATE).render(**template_input))
    except Exception:
        print(exceptions.text_error_template().render())
        return 1

    return 0


if __name__ == '__main__':
    sys.exit(main())
